# 조건문을 배워보자

print('\n')

print(1)
if True :
    print(2)
    print(3)
print(4)

print('\n')

print(1)
if False:
    print(2)
    print(3)
print(4)

print('\n')
print(1)
if True:
    print(2.1)
    print(3.1)
else:
    print(2.2)
    print(3.2)
print(4)

print('\n')
print(1)
if False:
    print(2.1)
    print(3.1)
else:
    print(2.2)
    print(3.2)
print(4)